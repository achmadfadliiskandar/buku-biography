        <p style="text-indent: 1em; text-align:justify;">Ia menyatakan bahwa materi terdiri atas atom yang tidak dapat
            dibagi lagi. Tiap-tiap unsur terdiri atas atom-atom dengan sifat dan massa identik, dan senyawa terbentuk
            jika atom dari berbagai unsur bergabung dalam komposisi yang tetap.</p>
        <p style="text-indent: 1em; text-align:justify;">John Dalton-lah ilmuwan Inggris yang di awal abad ke-19
            mengedepankan hipotesa atom ke dalam kancah ilmu pengetahuan. Dengan perbuatan ini, dia menyuguhkan ide
            kunci yang memungkinkan kemajuan besar di bidang kimia sejak saat itu.</p>
        <p style="text-indent: 1em; text-align:justify;">Supaya jelas, dia bukanlah orang pertama yang beranggapan bahwa
            semua obyek material terdiri dari sejumlah besar partikel yang teramat kecil dan tak terusakkan yang disebut
            atom. Pendapat ini sudah pernah diajukan oleh filosof Yunani kuno, Democritus, bahkan mungkin lebih dini
            lagi. Hipotesa itu diterima oleh Epicurus (filosof Yunani lainnya), dan dikedepankan secara brilian oleh
            penulis Romawi, Lucretius (meninggal tahun 55 SM), dalam dia punya syair yang masyhur “De rerum natura”
            (Tentang hakikat benda).</p>
        <p style="text-indent: 1em; text-align:justify;">Teori Democritus (yang tidak diterima oleh Aristoteles) tidak
            diacuhkan orang selama Abad Pertengahan, dan punya sedikit pengaruh terhadap ilmu pengetahuan. Meski begitu,
            beberapa ilmuwan terkemuka dari abad ke-17 (termasuk Isaac Newton) mendukung pendapat serupa. Tetapi, tak
            ada teori atom dikemukakan ataupun digunakan dalam penyelidikan ilmiah. Dan lebih penting lagi, tak ada
            seorang pun yang melihat adanya hubungan antara spekulasi filosofis tentang atom dengan hal-hal nyata di
            bidang kimia.</p>
        <p style="text-indent: 1em; text-align:justify;">Itulah keadaannya tatkala Dalton muncul. Dia menyuguhkan “teori
            kuantitatif” yang jelas dan jemih yang dapat digunakan dalam penafsiran percobaan kimia, dan dapat dicoba
            secara tepat di laboratorium.</p>
        <p style="text-indent: 1em; text-align:justify;">Meskipun terminologinya agak sedikit berbeda dengan yang kita
            gunakan sekarang, Dalton dengan jelas mengemukakan konsep tentang atom, molekul, elemen dan campuran kimia.
            Dia perjelas itu bahwa meski jumlah total atom di dunia sangat banyak, tetapi jumlah dari pelbagai jenis
            yang berbeda agak kecil. (Buku aslinya mencatat 20 elemen atau kelompok atom; kini sedikit di atas 100
            elemen sudah diketahui).</p>
