        <p style="text-indent: 1em; text-align:justify;">John Dalton adalah pencetus teori atom. Ia terkenal karena
            teorinya yang membangkitkan kembali istilah “atom”. Dalam buku karangannya yang berjudul New System of
            Chemical Philosophy ia berhasil merumuskan hal tentang atom sekitar tahun 1803. John Dalton hidup pada masa
